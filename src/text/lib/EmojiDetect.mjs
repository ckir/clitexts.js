const MANUAL_FIXES = [
    { from: '🏳‍🌈', to: '🏳️‍🌈' },
];
export const EMOJI_REGEX = new RegExp(`${FLAG_EMOTES.join('|')}|(\\p{EPres}|\\p{ExtPict})(\\u200d(\\p{EPres}|\\p{ExtPict})\\ufe0f?)*`, 'gu');

function* iterateCodePoints(str) {
    const matches = str.match(EMOJI_REGEX);
    if (matches) {
        for (let m of matches) {
            for (const fix of MANUAL_FIXES) {
                m = m.replace(fix.from, fix.to);
            }
            yield [...m].map(e => e.codePointAt(0));
        }
    }
}

export const detectCodePoints = (str) => {
    const emotes = [];
    for (const codePoints of iterateCodePoints(str)) {
        emotes.push(codePoints);
    }
    return emotes;
};

export const detectStrings = (str) => {
    const emotes = [];
    for (const codePoints of iterateCodePoints(str)) {
        emotes.push(codePoints.map(cp => cp.toString(16)).join('-'));
    }
    return emotes;
};

const EmojiDetect = {
    detectCodePoints: detectCodePoints,
    detectStrings: detectStrings,
    EMOJI_REGEX: EMOJI_REGEX,
};

export default EmojiDetect